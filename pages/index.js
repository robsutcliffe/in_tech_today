              <a href={post.href} key={post.id} target="_blank" className={styles.card}>
                <p>{post.blog}</p>
                <h2>{post.title}</h2>
                <ul>
                  {post.summary.map(item =>
                  <li key={item}>{item}</li>
                  )}
                </ul>
              </a>
